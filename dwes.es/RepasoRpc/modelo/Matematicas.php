<?php
    namespace RepasoRpc\modelo;;

    class Matematicas {
        // Metodo Suma
        public function suma($a, $b) {
            return $a + $b;
        }

        // Metodo Resta
        public function resta($a, $b) {
            return $a - $b;
        }

        // Metodo Multiplica
        public function multiplica($a, $b) {
            return $a * $b;
        }
    }   
?>